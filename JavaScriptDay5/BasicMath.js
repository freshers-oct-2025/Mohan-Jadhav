// round:- Rounds to the nearest integer.
console.log(Math.round(2.4));
console.log(Math.round(2.5));
console.log(Math.round(2.9));


//floor:- Rounds down to the nearest integer.
console.log(Math.floor(2.9));
console.log(Math.floor(-2.9));

//ceil:-Rounds up to the nearest integer.
console.log(Math.ceil(-2.9));
console.log(Math.ceil(2.9));


//trunc:-Removes the decimal part (keeps only the integer).
console.log(Math.trunc(4.8));
console.log(Math.trunc(-4.8));

//pow:- power of the number
console.log(Math.pow(4.8, 2));
console.log(Math.pow(-4.8, 5));
console.log(Math.pow(5, 2));

//sqrt:- squreroot of the number
console.log(Math.sqrt(4.8));

// abs:- positive value of the number 
console.log(Math.abs(4));
console.log(Math.abs(4));

//min:- obtain the minimum value from the list
console.log(Math.min(1,4,1,-1,-45))

//max:- obtain the maximum value from the list
console.log(Math.max(1,4,1,-1,-45))

//random
console.log(Math.random())

//pi
console.log(Math.PI)